    private void Emptable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Emptable1MouseClicked

        try {
            Seteditable();
            conn = SqlConnect2.conDB();
            int row = Emptable1.getSelectedRow();
            String ct = ( Emptable1.getModel().getValueAt(row, 0).toString());

            String p ="select ID,FirstName,LastName,Sex,Position,PhoneNo1,PhoneNo2,Email,Qualifications,Address,CourseTaken,Image from Lecturers where ID = '"+ct+"' ";
            st = conn.createStatement();
            rs = st.executeQuery(p);

            while(rs.next()){
                id = rs.getString("ID");
                fn = rs.getString("FirstName");
                ln = rs.getString("LastName");
                sex = rs.getString("Sex");
                pos = rs.getString("Position");
                ph1 = rs.getString("PhoneNo1");
                ph2 = rs.getString("PhoneNo2");
                email = rs.getString("Email");
                qual = rs.getString("Qualifications");
                addr = rs.getString("Address");
                cst = rs.getString("CourseTaken");
                b= rs.getBytes("Image");
            }
                IDTextField.setText(id);
                FirstNameTextField.setText(fn);
                LastNameTextField.setText(ln);
                SexComboBox.setSelectedItem(sex);
                PositionComboBox.setSelectedItem(pos);
                PhoneNo1.setText(ph1);
                PhoneNo2.setText(ph2);
                Email.setText(email);
                txtQualifications.setText(qual);
                Address.setText(addr);
                ImageLabel.setIcon(ResizeImage(b));
                jTextArea1.setText(cst);      
                         IDTextField.setEditable(false);
                       FirstNameTextField.setEditable(false);      
                            LastNameTextField.setEditable(false);
                         PhoneNo1.setEditable(false);
                         PhoneNo2.setEditable(false);
                       Email.setEditable(false);
                       txtQualifications.setEditable(false);
                       Address.setEditable(false);
                         jTextArea1.setEditable(false);
rs.close();
st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        } catch (IOException | ClassNotFoundException | InstantiationException | IllegalAccessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        finally{
            if (conn!= null){
                try {
                    conn.close();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex);
                }
            }

        }
    }//GEN-LAST:event_Emptable1MouseClicked

