    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if((FirstNameTextField.getText().equals(""))||
            (MiddleNameTextField.getText().equals(""))||(LastNameTextField.getText().equals("")) || (matnotb.getText().equals(""))||
            (CourseComboBox.getSelectedItem().equals("Select a Course...")) ||(LevelComboBox.getSelectedItem().equals("Choose  Level..."))  ){

            JOptionPane.showMessageDialog(null, " No Record To Save !  ");
            return;
        }

        try{
           
           
            String add = "update  tblstudents set  Level_100_FirstSemester_GP ='"+ GPFs100jTextField.getText() + "', Level_100_SecondSemester_GP ='"+ GPSs100jTextField.getText()  + "',   Level_100_Final_CGPA ='"+ CGPA100jTextField.getText()  + "',  Level_200_FirstSemester_GP ='"+ GPFs200jTextField.getText() + "',  Level_200_SecondSemester_GP ='"+ GPSs200jTextField.getText() + "', Level_200_Final_CGPA ='"+ CGPA200jTextField.getText() + "', Level_300_FirstSemester_GP ='"+ GPFs300jTextField.getText()  + "', Level_300_SecondSemester_GP ='"+ GPSs300jTextField.getText() + "', Level_300_Final_CGPA ='"+ CGPA300jTextField.getText() + "', Level_400_FirstSemester_GP ='"+ GPFs400jTextField.getText() + "' ,  Level_400_SecondSemester_GP ='"+GPSs400jTextField.getText()  + "',  Level_400_Final_CGPA ='"+ CGPA400jTextField.getText() + "'  where ID ='"+IDTextField.getText()+"' ";
             pst = conn.prepareStatement(add);
            pst.execute();

            Resetform();
            JOptionPane.showMessageDialog( this, "Record Saved","Student Record", JOptionPane.INFORMATION_MESSAGE);

            
        }catch(HeadlessException | SQLException ex){
           JOptionPane.showMessageDialog(null, ex);
        } finally{
            if ((conn!= null) || (conn == null)){
                try {
                    pst.close();
                    conn.close();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex);
                }
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

